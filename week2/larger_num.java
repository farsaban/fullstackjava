package week2;

public class larger_num {
	public static void main(String[] args) {
		double n1=0, n2=7.8,n3=-2.5;
		if(n1 >= n2 && n1 >= n3)
			System.out.println(n1 +"is larger number");
		else if(n2 >= n1 && n2 >= n3)
			System.out.println(n2 +"is larger number");
		else
			System.out.println(n3 +"is larger number");
			
	}

}
